#!/usr/bin/env node
import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT = join(__dirname, '..', 'app', 'src', 'data', 'changelog.ts');
const MAX_ENTRIES = 50;

const ALLOWED_PREFIXES = {
  'feat':   'feature',
  'data':   'data',
  'docs':   'docs',
  'fix':    'fix',
  'update': 'update',
};

function getGitLog() {
  try {
    const raw = execSync(
      `git log --format="%h|%ad|%s" --date=short -n 200`,
      { encoding: 'utf-8', cwd: join(__dirname, '..') },
    );
    return raw.trim().split('\n').filter(Boolean);
  } catch {
    return [];
  }
}

function parseLine(line) {
  const [id, date, ...rest] = line.split('|');
  const subject = rest.join('|').trim();

  for (const [prefix, type] of Object.entries(ALLOWED_PREFIXES)) {
    const re = new RegExp(`^${prefix}(\\([^)]+\\))?:\\s*`);
    if (re.test(subject)) {
      return { id, date, message: subject.replace(re, ''), type };
    }
  }
  return null;
}

function readExistingEntries() {
  try {
    if (!existsSync(OUTPUT)) return [];
    const src = readFileSync(OUTPUT, 'utf-8');
    const match = src.match(/export const changelog:\s*ChangelogEntry\[\]\s*=\s*(\[[\s\S]*?\n\]);/);
    if (!match) return [];
    return new Function(`return ${match[1]}`)();
  } catch (err) {
    console.warn('changelog: could not parse existing file, starting fresh:', err.message);
    return [];
  }
}

const gitEntries = getGitLog().map(parseLine).filter(Boolean);
const existing = readExistingEntries();

const existingIds = new Set(existing.map(e => e.id));
const gitIds = new Set(gitEntries.map(e => e.id));

const newFromGit = gitEntries.filter(e => !existingIds.has(e.id));
const existingById = new Map(existing.map(e => [e.id, e]));

// For entries that exist in both git and file, keep the file version (curated Hebrew text wins)
const merged = [
  ...newFromGit,
  ...existing.map(e => existingById.get(e.id)),
].filter(Boolean).slice(0, MAX_ENTRIES);

if (JSON.stringify(merged) === JSON.stringify(existing)) {
  console.log('changelog: no changes');
  process.exit(0);
}

const ts = `// Auto-generated by scripts/generate-changelog.js — do not edit manually
export interface ChangelogEntry {
  id: string;
  date: string;
  message: string;
  type: 'feature' | 'data' | 'docs' | 'fix' | 'update';
}

export const changelog: ChangelogEntry[] = ${JSON.stringify(merged, null, 2)};
`;

writeFileSync(OUTPUT, ts, 'utf-8');
console.log(`changelog: wrote ${merged.length} entries (${newFromGit.length} new from git, ${existing.length} curated) → app/src/data/changelog.ts`);
